/**
 * Type-safe helper to create Prismocker with ecosystem support
 *
 * This is a convenience function that creates a PrismockerClient with
 * ecosystem compatibility options pre-configured.
 *
 * @param options - Prismocker and ecosystem options
 * @returns PrismockerClient instance with ecosystem support
 *
 * @example
 * ```typescript
 * const prisma = createPrismockerWithEcosystem({
 *   validateWithZod: true,
 *   zodSchemasPath: '@prisma/zod',
 * });
 * ```
 */
export function createPrismockerWithEcosystem<T extends PrismaClient>(
  options?: import('./types.js').PrismockerOptions
): T {
  // Import createPrismocker dynamically to avoid circular dependencies
  // eslint-disable-next-line @typescript-eslint/no-require-imports
  const { createPrismocker } = require('./index.js');
  return createPrismocker(options) as T;
}
