#!/usr/bin/env node

/**
 * Prismocker Enum Stub Generator
 *
 * This CLI tool parses a Prisma schema file and generates enum stub exports
 * for Vitest mock files to support automatic ENUM mocking.
 *
 * Usage:
 *   npx prismocker generate-enums [schema-path] [mock-file-path]
 *
 * Options:
 *   --schema <path>     Path to Prisma schema file (default: ./prisma/schema.prisma)
 *   --mock <path>       Path to mock file (default: ./__mocks__/@prisma/client.ts)
 *   --help              Show help message
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { join, dirname, resolve } from 'node:path';
import { existsSync } from 'node:fs';

interface EnumDefinition {
  name: string;
  values: Array<{ key: string; value: string }>;
}

/**
 * Parse Prisma schema to extract enum definitions
 * Handles @map() directives to get actual database values
 */
function parsePrismaEnums(schemaContent: string): EnumDefinition[] {
  const enums: EnumDefinition[] = [];

  // Match enum blocks: enum name { ... }
  const enumRegex = /enum\s+(\w+)\s*\{([^}]+)\}/g;
  let match;

  while ((match = enumRegex.exec(schemaContent)) !== null) {
    const enumName = match[1];
    const enumBody = match[2];

    // Parse enum values (handles @map() directives)
    const valueRegex = /(\w+)(?:\s+@map\(["']([^"']+)["']\))?/g;
    const values: Array<{ key: string; value: string }> = [];
    let valueMatch;

    while ((valueMatch = valueRegex.exec(enumBody)) !== null) {
      const key = valueMatch[1];
      // If @map() exists, use mapped value; otherwise use key as value
      const value = valueMatch[2] || key;
      values.push({ key, value });
    }

    if (values.length > 0) {
      enums.push({ name: enumName, values });
    }
  }

  return enums;
}

/**
 * Generate enum stub export code
 */
function generateEnumStub(enumDef: EnumDefinition): string {
  const entries = enumDef.values
    .map(({ key, value }) => `  ${key}: ${JSON.stringify(value)}`)
    .join(',\n');

  return `export const ${enumDef.name} = {\n${entries},\n} as const;`;
}

/**
 * Show help message
 */
function showHelp() {
  console.log(`
Prismocker Enum Stub Generator

Usage:
  npx prismocker generate-enums [options]

Options:
  --schema <path>     Path to Prisma schema file (default: ./prisma/schema.prisma)
  --mock <path>       Path to mock file (default: ./__mocks__/@prisma/client.ts)
  --help              Show this help message

Examples:
  npx prismocker generate-enums
  npx prismocker generate-enums --schema ./prisma/schema.prisma --mock ./__mocks__/@prisma/client.ts

This tool generates enum stub exports for Vitest mock files to support
automatic ENUM mocking when using Prismocker with Vitest.
`);
}

/**
 * Main function
 */
async function main() {
  const args = process.argv.slice(2);

  // Parse arguments
  let schemaPath = './prisma/schema.prisma';
  let mockPath = './__mocks__/@prisma/client.ts';

  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    if (arg === '--help' || arg === '-h') {
      showHelp();
      process.exit(0);
    } else if (arg === '--schema' && i + 1 < args.length) {
      schemaPath = args[++i];
    } else if (arg === '--mock' && i + 1 < args.length) {
      mockPath = args[++i];
    }
  }

  // Resolve paths relative to current working directory
  const cwd = process.cwd();
  const resolvedSchemaPath = resolve(cwd, schemaPath);
  const resolvedMockPath = resolve(cwd, mockPath);

  try {
    // Check if schema file exists
    if (!existsSync(resolvedSchemaPath)) {
      console.error(`❌ Error: Schema file not found: ${resolvedSchemaPath}`);
      console.error(`   Please provide a valid path to your Prisma schema file.`);
      process.exit(1);
    }

    // Read Prisma schema
    const schemaContent = await readFile(resolvedSchemaPath, 'utf-8');

    // Parse enums
    const enums = parsePrismaEnums(schemaContent);

    if (enums.length === 0) {
      console.log('⚠️  No enums found in schema file');
      process.exit(0);
    }

    console.log(`Found ${enums.length} enums in schema`);

    // Check if mock file exists
    const mockFileExists = existsSync(resolvedMockPath);
    let mockContent = '';

    if (mockFileExists) {
      // Read existing mock file
      mockContent = await readFile(resolvedMockPath, 'utf-8');
    } else {
      // Create basic mock file structure if it doesn't exist
      console.log(`⚠️  Mock file not found: ${resolvedMockPath}`);
      console.log(`   Creating new mock file...`);

      mockContent = `/**
 * Prismocker Mock for @prisma/client
 *
 * This file is auto-generated by Prismocker's enum generator.
 * It exports PrismockerClient as PrismaClient and includes enum stubs.
 */

import { createPrismocker } from '@jsonbored/prismocker';
import type { PrismaClient } from '@prisma/client';

// Create PrismockerClient instance
const PrismockerClientClass = createPrismocker<PrismaClient>();

// Export as PrismaClient for Vitest auto-mocking
export { PrismockerClientClass as PrismaClient };

// Export Prisma namespace (for Prisma.Decimal, etc.)
export const Prisma = {
  Decimal: class Decimal {
    value: any;
    constructor(value: any) {
      this.value = value;
    }
    toString() {
      return String(this.value);
    }
    toNumber() {
      return Number(this.value);
    }
    toFixed(decimalPlaces?: number) {
      return Number(this.value).toFixed(decimalPlaces);
    }
    toJSON() {
      return this.value;
    }
  },
};

// Export Prisma enum stubs to prevent Vitest from trying to load the actual @prisma/client module
// These are stub objects that match the structure of Prisma-generated enums
// When code imports \`import { enum_name } from '@prisma/client'\`, Vitest will use these stubs
// instead of trying to load the actual module (which requires .prisma/client/default to exist)
//
// These enums are defined in prisma/schema.prisma and generated by Prisma
// The stub values match the enum keys from the schema
// NOTE: This section is auto-generated by \`npx prismocker generate-enums\`
// Run this command after adding/modifying enums in your Prisma schema

`;
    }

    // Find the section where enums are exported
    const enumStartMarker = '// Export Prisma enum stubs';
    const enumEndMarker = '// NOTE: This section is auto-generated';

    let startIndex = mockContent.indexOf(enumStartMarker);
    let endIndex = mockContent.indexOf(enumEndMarker, startIndex);

    // If markers not found, try alternative markers
    if (startIndex === -1) {
      const altMarker = '// Export Prisma enum stubs to prevent Vitest';
      startIndex = mockContent.indexOf(altMarker);
      if (startIndex !== -1) {
        endIndex = mockContent.indexOf('// NOTE:', startIndex);
      }
    }

    // If still not found, append to end of file
    if (startIndex === -1 || endIndex === -1) {
      // Append enum section to end of file
      const enumExports = enums.map(generateEnumStub).join('\n\n');
      const newSection = `\n\n// Export Prisma enum stubs to prevent Vitest from trying to load the actual @prisma/client module
// These are stub objects that match the structure of Prisma-generated enums
// When code imports \`import { enum_name } from '@prisma/client'\`, Vitest will use these stubs
// instead of trying to load the actual module (which requires .prisma/client/default to exist)
//
// These enums are defined in prisma/schema.prisma and generated by Prisma
// The stub values match the enum keys from the schema
// NOTE: This section is auto-generated by \`npx prismocker generate-enums\`
// Run this command after adding/modifying enums in your Prisma schema

${enumExports}
`;
      mockContent += newSection;
    } else {
      // Replace enum section
      const beforeEnums = mockContent.substring(0, startIndex);
      const afterEnums = mockContent.substring(endIndex);

      const enumExports = enums.map(generateEnumStub).join('\n\n');

      mockContent = `${beforeEnums}// Export Prisma enum stubs to prevent Vitest from trying to load the actual @prisma/client module
// These are stub objects that match the structure of Prisma-generated enums
// When code imports \`import { enum_name } from '@prisma/client'\`, Vitest will use these stubs
// instead of trying to load the actual module (which requires .prisma/client/default to exist)
//
// These enums are defined in prisma/schema.prisma and generated by Prisma
// The stub values match the enum keys from the schema
// NOTE: This section is auto-generated by \`npx prismocker generate-enums\`
// Run this command after adding/modifying enums in your Prisma schema

${enumExports}

${afterEnums}`;
    }

    // Ensure directory exists
    await mkdir(dirname(resolvedMockPath), { recursive: true });

    // Write updated mock file
    await writeFile(resolvedMockPath, mockContent, 'utf-8');

    console.log(`✅ Generated ${enums.length} enum stubs in ${mockPath}`);
    console.log(`   Enums: ${enums.map((e) => e.name).join(', ')}`);
  } catch (error) {
    console.error('❌ Error generating enum stubs:', error);
    process.exit(1);
  }
}

// Run if called directly
main();
